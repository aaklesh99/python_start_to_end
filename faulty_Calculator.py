num1 = int(input("Enter first no :"))
num2= int(input("Enter second no :"))
operation = input("Enter operation (+,-,*,/) : ")
#for Addition
if(num1==56 and num2==9 and operation== "+"):
    print("Sum =77")
elif(num1==56 and num2==9 and operation== "-"):
    print("Sub =908")
elif(num1==45 and num2==3 and operation== "*"):
    print("Mul =555")
elif(num1==56 and num2==6 and operation== "/"):
    print("Div =4")
elif operation=="+":
    print(num1+num2)
elif operation=="-":
    print(num1-num2)
elif operation == "*":
    print(num1*num2)
elif operation=="/":
    print(num1/num2)
